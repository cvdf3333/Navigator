def calc_isa_tax_benefit(annual_income: int, deposit_amount: int, isa_type: str = "general") -> dict:
    """ISA 세액공제 계산"""
    limit = 2000
    actual_deposit = min(deposit_amount, limit)

    tax_free_limit = 400 if isa_type == "general" else 400
    if annual_income <= 3800:
        tax_free_limit = 400

    assumed_return_rate = 0.06
    expected_profit = actual_deposit * assumed_return_rate

    taxable_profit = max(expected_profit - tax_free_limit, 0)
    isa_tax = taxable_profit * 0.099
    normal_tax = expected_profit * 0.154
    tax_saving = normal_tax - isa_tax

    return {
        "deposit": actual_deposit,
        "expectedProfit": round(expected_profit, 1),
        "taxFreeLimit": tax_free_limit,
        "normalTax": round(normal_tax, 1),
        "isaTax": round(isa_tax, 1),
        "taxSaving": round(tax_saving, 1),
        "effectiveRate": round((tax_saving / expected_profit * 100) if expected_profit > 0 else 0, 1),
    }


def calc_irp_tax_benefit(annual_income: int, deposit_amount: int) -> dict:
    """IRP/연금저축 세액공제 계산"""
    irp_limit = 900
    pension_limit = 600

    irp_deposit = min(deposit_amount, irp_limit)

    if annual_income <= 5500:
        rate = 0.165
    else:
        rate = 0.132

    tax_deduction = round(irp_deposit * rate, 1)

    return {
        "deposit": irp_deposit,
        "rate": round(rate * 100, 1),
        "taxDeduction": tax_deduction,
        "monthlyAmount": round(irp_deposit / 12, 1),
        "effectiveMonthly": round(tax_deduction / 12, 1),
        "pensionLimit": pension_limit,
        "irpLimit": irp_limit,
    }
